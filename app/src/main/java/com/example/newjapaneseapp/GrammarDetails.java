package com.example.newjapaneseapp;

public class GrammarDetails {

    private String grammar, grammarDes;

    public GrammarDetails(String grammar, String grammarDes) {
        this.grammar = grammar;
        this.grammarDes = grammarDes;
    }

    public String getGrammar() {
        return grammar;
    }

    public void setGrammar(String grammar) {
        this.grammar = grammar;
    }

    public String getGrammarDes() {
        return grammarDes;
    }

    public void setGrammarDes(String grammarDes) {
        this.grammarDes = grammarDes;
    }
}
